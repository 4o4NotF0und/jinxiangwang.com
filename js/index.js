//大轮播图
$('.slider').bscSlider({
    autoplay        : true,
    navigation      : false,
    duration        : 4000,
    effect          : 1,
    effect_speed    : 750,
    easing          : 'swing',
    height          : 453
  });   
    // autoplay：轮播图是否自动播放。
    // navigation：轮播图是否显示前后导航按钮。
    // duration：轮播图图片切换的间隔时间，单位毫秒。
    // effect：内置的过渡动画效果，可选值为1-17。
    // effect_speed：过渡动画的速度，单位毫秒。
    // easing：easing动画函数。
    // height：指定轮播图的高度。            